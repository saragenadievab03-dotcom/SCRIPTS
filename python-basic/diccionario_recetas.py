# diccionario_recetas.py

recetas = {
    "tarta": ["harina", "huevos", "azúcar"],
    "pizza": ["masa", "tomate", "queso"],
    "ensalada": ["lechuga", "tomate", "aceite"]
}

for r in recetas:
    print(r, ":", recetas[r])
  

# resultado
tarta : ['harina', 'huevos', 'azúcar']
pizza : ['masa', 'tomate', 'queso']
ensalada : ['lechuga', 'tomate', 'aceite']


